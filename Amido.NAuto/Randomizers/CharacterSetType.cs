namespace Amido.NAuto.Randomizers
{
    /// <summary>
    /// Enumeration for setting the character set.
    /// </summary>
    public enum CharacterSetType
    {
        /// <summary>
        /// Any character set.
        /// </summary>
        Anything,

        /// <summary>
        /// Alpha only character set.
        /// </summary>
        Alpha,

        /// <summary>
        /// Alpha numeric only character set.
        /// </summary>
        AlphaNumeric,

        /// <summary>
        /// Numeric character set.
        /// </summary>
        Numeric
    }
}